    def _create_interleaved_data(self, df_main, df_insert):
        print("Creating periodically interleaved dataset...")
        points_per_day = 24 * 4

        period_points = self.interleave_period_days * points_per_day
        duration_points = self.interleave_duration_days * points_per_day
        normal_points = period_points - duration_points

        interleaved_chunks = []
        main_cursor, insert_cursor = 0, 0

        while main_cursor < len(df_main):
            # 1. Add a segment of the main dataset
            main_end = main_cursor + normal_points
            chunk_main = df_main.iloc[main_cursor:main_end]
            if chunk_main.empty: break
            interleaved_chunks.append(chunk_main)
            main_cursor = main_end

            # 2. Add a segment from the insert dataset
            if insert_cursor >= len(df_insert):
                print("Insert dataset exhausted. Appending remaining main data.")
                remaining_main = df_main.iloc[main_cursor:]
                if not remaining_main.empty:
                    interleaved_chunks.append(remaining_main)
                break

            insert_end = insert_cursor + duration_points
            chunk_insert = df_insert.iloc[insert_cursor:insert_end].copy()
            if chunk_insert.empty: break

            # Re-align timestamps
            last_main_timestamp = interleaved_chunks[-1]['date'].iloc[-1]
            new_timestamps = pd.to_datetime(
                pd.date_range(start=last_main_timestamp, periods=len(chunk_insert) + 1, freq='15min')[1:])
            chunk_insert['date'] = new_timestamps

            interleaved_chunks.append(chunk_insert)
            insert_cursor = insert_end
            main_cursor += duration_points

        return pd.concat(interleaved_chunks, ignore_index=True)

