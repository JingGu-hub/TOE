class Dataset_ETTm_Interleave_m1_m2(Dataset):

    def __init__(self, root_path, flag='train', size=None,
                 features='S', data_path='ETTm1.csv',
                 target='OT', scale=True, timeenc=0, freq='t', train_only=False,
                 # Parameters to control the interleaving logic
                 interleave_period_days=30,
                 interleave_duration_days=7, args=None):
        # size [seq_len, label_len, pred_len]
        if size is None:
            self.seq_len = 24 * 4 * 4
            self.label_len = 24 * 4
            self.pred_len = 24 * 4
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]

        assert flag in ['train', 'test', 'val']
        type_map = {'train': 0, 'val': 1, 'test': 2}
        self.set_type = type_map[flag]

        self.features = features
        self.target = target
        self.scale = scale
        self.timeenc = timeenc
        self.freq = freq
        self.train_only = train_only

        self.root_path = root_path
        self.data_path_m1 = 'ETTm1.csv'
        self.data_path_m2 = 'ETTm2.csv'

        self.interleave_period_days = interleave_period_days
        self.interleave_duration_days = interleave_duration_days

        self.__read_data__()

    def _create_interleaved_data(self, df_m1, df_m2):
        """ Core function to create the interleaved dataset """
        print("Creating periodically interleaved dataset...")
        points_per_day = 24 * 4

        # Convert days to number of data points
        period_points = self.interleave_period_days * points_per_day
        duration_points = self.interleave_duration_days * points_per_day
        normal_points = period_points - duration_points

        interleaved_chunks = []
        m1_cursor, m2_cursor = 0, 0

        while m1_cursor < len(df_m1):
            # 1. Add a normal segment from m1
            m1_end = m1_cursor + normal_points
            chunk_m1 = df_m1.iloc[m1_cursor:m1_end]
            if chunk_m1.empty: break
            interleaved_chunks.append(chunk_m1)
            m1_cursor = m1_end

            # 2. Add an "interference" segment from m2
            if m2_cursor >= len(df_m2):
                print("ETTm2 data exhausted. Appending remaining ETTm1 data.")
                remaining_m1 = df_m1.iloc[m1_cursor:]
                if not remaining_m1.empty:
                    interleaved_chunks.append(remaining_m1)
                break

            m2_end = m2_cursor + duration_points
            chunk_m2 = df_m2.iloc[m2_cursor:m2_end].copy()
            if chunk_m2.empty: break

            # Re-align timestamps of the m2 chunk to follow the m1 timeline
            last_m1_timestamp = interleaved_chunks[-1]['date'].iloc[-1]
            new_timestamps = pd.to_datetime(
                pd.date_range(start=last_m1_timestamp, periods=len(chunk_m2) + 1, freq='15min')[1:])
            chunk_m2['date'] = new_timestamps

            interleaved_chunks.append(chunk_m2)
            m2_cursor = m2_end

            # Advance the m1 cursor past the replaced time period
            m1_cursor += duration_points

        return pd.concat(interleaved_chunks, ignore_index=True)

    def __read_data__(self):
        self.scaler = StandardScaler()
        df_raw_m1 = pd.read_csv(os.path.join(self.root_path, self.data_path_m1))
        df_raw_m2 = pd.read_csv(os.path.join(self.root_path, self.data_path_m2))

        df_raw_m1['date'] = pd.to_datetime(df_raw_m1['date'])
        df_raw_m2['date'] = pd.to_datetime(df_raw_m2['date'])

        # 1. Create the interleaved data
        df_raw = self._create_interleaved_data(df_raw_m1, df_raw_m2)
        print(f"Interleaving complete. New dataset length: {len(df_raw)}")

        # 2. Split the newly generated dataset (70/10/20 split)
        num_train = int(len(df_raw) * (0.7 if not self.train_only else 1))
        num_test = int(len(df_raw) * 0.2)
        num_vali = len(df_raw) - num_train - num_test

        border1s = [0, num_train - self.seq_len, len(df_raw) - num_test - self.seq_len]
        border2s = [num_train, num_train + num_vali, len(df_raw)]
        border1 = border1s[self.set_type]
        border2 = border2s[self.set_type]

        if self.features == 'M' or self.features == 'MS':
            cols_data = df_raw.columns[1:]
            df_data = df_raw[cols_data]
        elif self.features == 'S':
            df_data = df_raw[[self.target]]

        # 3. Standardization
        if self.scale:
            train_data = df_data.iloc[border1s[0]:border2s[0]].values
            self.scaler.fit(train_data)
            data = self.scaler.transform(df_data.values)
        else:
            data = df_data.values

        # 4. Time feature encoding
        df_stamp = df_raw[['date']][border1:border2]
        df_stamp['date'] = pd.to_datetime(df_stamp['date'])

